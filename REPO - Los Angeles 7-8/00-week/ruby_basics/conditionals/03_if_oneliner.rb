# Modifier fun...

# Ruby if modifier	

# Syntax:
#
#		code if condition

# Executes code if the conditional is true.

		puts 'one line if statement' if true 

# "if" is what is called a 'statement modifier' in Ruby and because of this we can do things like this

		if true
			puts 'another one line if statement'
		end

# go ahead and press alt+shift+u
# >> one line if statement
# >> another one line if statement