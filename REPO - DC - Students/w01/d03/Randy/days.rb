puts "What day is it?"
response = gets.chomp.downcase
case response
	when "monday"
		puts "Uhg Mondays, am i Right?"
	when "tuesday"
		puts "So much week left :("
	when "wednesday"
		puts "HumpDayHumpDay"
	when "thursday"
		puts "Almost there...."
	when "friday"
		puts "TGIFMF!"
	when "saturday"
		puts "Get busy!"
	when "sunday"
		puts "Drunk brunch!"
	else
		puts "Try again."
	end

		
		