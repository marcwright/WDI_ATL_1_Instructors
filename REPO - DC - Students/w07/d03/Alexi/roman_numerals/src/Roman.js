function romanize(arabNum) {
  var romanNum;

  while (arabNum <= 4) {
    if (arabNum === 1) {
      this.romanNum = 'I';
    } else if (arabNum === 2) {
      this.romanNum = 'II';
    } else {
      (arabNum === 3)
      this.romanNum = 'III'
    }
    return this.romanNum;
  }

}
