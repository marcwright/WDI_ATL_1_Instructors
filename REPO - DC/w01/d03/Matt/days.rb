# Days of the week

puts "What day is it today?"
today = gets.chomp

if today == "Monday"
  puts "Mad for Monday"
elsif today == "Tuesday"
  puts "Two for Tuesday"
elsif today == "Wednesday"
  puts "Wild for Wednesday"
elsif today == "Thursday"
  puts "Thirst for Thursday"
elsif today == "Friday"
  puts "Fun for Friday"
else
  puts "That is not a weekday"
end
