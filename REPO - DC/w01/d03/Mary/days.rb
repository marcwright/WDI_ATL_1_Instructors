puts "What day of the week is it?"
day = gets.chomp.capitalize

case day
when "Monday"
  puts "Ugh, Monday....good luck."
when "Tuesday"
  puts "Tuesdays are pretty rotten, huh."
when "Wednesday"
  puts "Well, have a Happy Hump Day!"
when "Thursday"
  puts "At least it is almost Friday?"
when "Friday"
  puts "Fridayyyy is heeeere!"
when "Saturday"
  puts "Yes! What we have been waiting for all week!!!"
when "Sunday"
  puts "First one to mention the work week gets a swirly."
end
